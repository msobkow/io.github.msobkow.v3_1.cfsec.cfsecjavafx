	public class SecRoleByQualNameComparator
	implements Comparator<ICFSecSecRoleObj>
	{
		public SecRoleByQualNameComparator() {
		}

		public int compare( ICFSecSecRoleObj lhs, ICFSecSecRoleObj rhs ) {
			if( lhs == null ) {
				if( rhs == null ) {
					return( 0 );
				}
				else {
					return( -1 );
				}
			}
			else if( rhs == null ) {
				return( 1 );
			}
			else {
				String lhsValue = lhs.getObjQualifiedName();
				String rhsValue = rhs.getObjQualifiedName();
				if( lhsValue == null ) {
					if( rhsValue == null ) {
						return( 0 );
					}
					else {
						return( -1 );
					}
				}
				else if( rhsValue == null ) {
					return( 1 );
				}
				else {
					return( lhsValue.compareTo( rhsValue ) );
				}
			}
		}
	}

	protected SecRoleByQualNameComparator compareSecRoleByQualName = new SecRoleByQualNameComparator();

